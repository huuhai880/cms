CREATE PROCEDURE FOR_FORMULA_CONDITION_GetList_AdminWeb
    @FORMULAID BIGINT = 0
AS
BEGIN

    SELECT 
            FORMULAID,
            CONDITIONFORMULAID,
            ATTRIBUTESGROUPID,
            [VALUE],
            ISFOMULAORTHERID,
            ORTHERID
    FROM    FOR_FORMULA_CONDITION
    WHERE   ISACTIVE = 1
    AND     ISDELETED = 0
    AND     FORMULAID = @FORMULAID

END
