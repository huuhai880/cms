CREATE PROCEDURE AFF_AFFILIATE_UpdateStatus_AdminWeb
    @AFFILIATEID BIGINT = 0,
    @ISACTIVE BIT = 0,
    @UPDATEDUSER VARCHAR(20) = NULL 
AS 
BEGIN

    UPDATE  AFF_AFFILIATE
    SET     UPDATEDUSER = @UPDATEDUSER,
            UPDATEDDATE = GETDATE(),
            ISACTIVE = @ISACTIVE
    WHERE   AFFILIATEID = @AFFILIATEID

END