CREATE PROCEDURE FOR_FORMULA_CONDITION_Delete_AdminWeb
    @FORMULAID BIGINT = 0,
    @DELETEDUSER VARCHAR(50) = NULL
AS
BEGIN

    UPDATE  FOR_FORMULA_CONDITION
    SET     ISDELETED = 1,
            DELETEDDATE = GETDATE(),
            DELETEDUSER = @DELETEDUSER
    WHERE   FORMULAID = @FORMULAID

END