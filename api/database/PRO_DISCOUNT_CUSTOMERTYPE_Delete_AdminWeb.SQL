ALTER PROCEDURE [dbo].[PRO_DISCOUNT_CUSTOMERTYPE_Delete_AdminWeb]
    @DISCOUNTID INT = NULL,
    @DELETEDUSER VARCHAR(50) = NULL
AS
BEGIN
		UPDATE PRO_DISCOUNT_CUSTOMERTYPE 
        SET 
                ISDELETED = 1,
                DELETEDDATE= GETDATE(),
                DELETEDUSER = @DELETEDUSER
		WHERE    DISCOUNTID = @DISCOUNTID
END
GO
