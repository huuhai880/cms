ALTER PROCEDURE PRO_COMMENT_Delete_AdminWeb
    @PRODUCTCOMMENTID BIGINT = 0,
    @DELETEDUSER VARCHAR(20) = NULL

AS
BEGIN
    UPDATE  PRO_COMMENT
    SET     ISDELETED = 1,
            DELETEDDATE = GETDATE(),
            DELETEDUSER = @DELETEDUSER
    WHERE   PRODUCTCOMMENTID = @PRODUCTCOMMENTID

END