CREATE PROCEDURE CRM_ACCOUNT_AFFILIATE_Review_AdminWeb
    @MEMBERID BIGINT = 0,
    @STATUSAFFILIATE INT = 0, --1: MOI, 2: DUYET: 4 KHONG DUYET
    @REVIEWNOTE NVARCHAR(500) = NULL,
    @REVIEWUSER VARCHAR(20) = NULL
AS 
BEGIN

    UPDATE  CRM_ACCOUNT
    SET     UPDATEDUSER = @REVIEWUSER,
            UPDATEDDATE = GETDATE(),
            STATUSAFFILIATE = @STATUSAFFILIATE,
            ISAFFILIATE = IIF(@STATUSAFFILIATE = 2, 1, ISAFFILIATE),
            DATEOFAPPROVAL = IIF(@STATUSAFFILIATE = 2, GETDATE(), DATEOFAPPROVAL),
            REVIEWUSER = @REVIEWUSER,
            REVIEWNOTE = @REVIEWNOTE
    WHERE   MEMBERID = @MEMBERID

END