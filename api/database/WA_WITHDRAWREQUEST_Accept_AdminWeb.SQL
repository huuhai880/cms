ALTER PROCEDURE WA_WITHDRAWREQUEST_Accept_AdminWeb
    @WDREQUESTID BIGINT = 0,
    @CONFIRMUSER VARCHAR(50) = NULL,
    @WDREQUESTNO VARCHAR(50) = NULL
AS 
BEGIN
    UPDATE  WA_WITHDRAWREQUEST
    SET     UPDATEDDATE = GETDATE(),
            UPDATEDUSER = @CONFIRMUSER,
            NOTE = N'Đồng ý duyệt yêu cầu rút tiền ' + @WDREQUESTNO,
            WDREQUESTSTATUS = 2,
            CONFIRMUSER = @CONFIRMUSER,
            WDDATECONFIRM = GETDATE()
    WHERE   WDREQUESTID = @WDREQUESTID
END