CREATE PROCEDURE CRM_ACCOUNT_AFFILIATE_UpLevel_AdminWeb
    @MEMBERIDS VARCHAR(500) = NULL,
    @UPDATEDUSER VARCHAR(20) = NULL
AS 
BEGIN

    DECLARE @AFFILIATE_TYPE_LEADER AS BIGINT = 0;
    SELECT 
            TOP 1 @AFFILIATE_TYPE_LEADER = AFFILIATETYPEID
    FROM    AFF_AFFILIATETYPE
    WHERE   AFF_AFFILIATETYPE.ISACTIVE = 1
    AND     AFF_AFFILIATETYPE.ISDELETED = 0
    AND     AFF_AFFILIATETYPE.ISAFFILIATELEVEL2 = 1 --LEADER
 
    UPDATE  CRM_ACCOUNT
    SET     UPDATEDDATE = GETDATE(),
            UPDATEDUSER = @UPDATEDUSER,
            AFFILIATETYPEID = @AFFILIATE_TYPE_LEADER
    WHERE   MEMBERID IN (SELECT CAST(ITEM AS BIGINT) FROM DBO.split(@MEMBERIDS, ','))

END