CREATE PROCEDURE FOR_FORMULA_CONDITION_Create_AdminWeb
    @FORMULARID BIGINT = 0,
    @ATTRIBUTESGROUPID BIGINT = 0,
    @VALUE VARCHAR(1000) = NULL,
    @ISFOMULAORTHERID BIT = 0,
    @ORTHERID BIGINT = 0,
    @CREATEDUSER VARCHAR(100) = NULL
AS
BEGIN

    INSERT INTO FOR_FORMULA_CONDITION
    (
        FORMULAID,
        ATTRIBUTESGROUPID,
        [VALUE],
        ISFOMULAORTHERID,
        ORTHERID,
        ISACTIVE,
        CREATEDDATE,
        CREATEDUSER,
        ISDELETED
    )
    VALUES
    (
        @FORMULARID,
        @ATTRIBUTESGROUPID,
        @VALUE,
        @ISFOMULAORTHERID,
        @ORTHERID,
        1,
        GETDATE(),
        @CREATEDUSER,
        0
    )

END