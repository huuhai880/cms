ALTER PROCEDURE CRM_ACCOUNT_AFFILIATE_Detail_AdminWeb
    @MEMBERID BIGINT = 0
AS 
BEGIN

      SELECT 
            CRM_ACCOUNT.MEMBERID,
            CRM_ACCOUNT.CUSTOMERCODE,
            CONCAT(CRM_ACCOUNT.CUSTOMERCODE, '-', CRM_ACCOUNT.FULLNAME) AS FULLNAME,
            CRM_ACCOUNT.EMAIL,
            CRM_ACCOUNT.PHONENUMBER,
            CRM_ACCOUNT.AFFILIATETYPEID,
            AFF_AFFILIATETYPE.AFFILIATETYPENAME,
            IIF(CRM_ACCOUNT.REGISTRATIONDATE IS NOT NULL, FORMAT(CRM_ACCOUNT.REGISTRATIONDATE, 'dd/MM/yyyy'), '') REGISTRATIONDATEVIEW,
            CRM_ACCOUNT.ISACTIVE,
            IIF(CRM_ACCOUNT.DATEOFAPPROVAL IS NOT NULL, FORMAT(CRM_ACCOUNT.DATEOFAPPROVAL, 'dd/MM/yyyy'), '') DATEOFAPPROVALVIEW

    FROM    CRM_ACCOUNT

    JOIN    AFF_AFFILIATETYPE
    ON      CRM_ACCOUNT.AFFILIATETYPEID = AFF_AFFILIATETYPE.AFFILIATETYPEID
    AND     AFF_AFFILIATETYPE.ISACTIVE = 1
    AND     AFF_AFFILIATETYPE.ISDELETED = 0

    WHERE   CRM_ACCOUNT.ISDELETED = 0
    AND     CRM_ACCOUNT.MEMBERID = @MEMBERID
    AND     CRM_ACCOUNT.ISAFFILIATE = 1

END