ALTER PROCEDURE [dbo].[MD_PRODUCT_ATTRIBUTES_Create_AdminWeb]
	@PROATTRIBUTESID bigint = 0,
    @PRODUCTID BIGINT = 0,
    @ATTRIBUTESGROUPID BIGINT = 0,
    @INTERPRETID BIGINT = 0,
    @INTERPRETDETAILID BIGINT = null,
    @ISSHOWSEARCHRESULT BIT = 0,
    @TEXTURL NVARCHAR(2000) = NULL,
    @URL NVARCHAR(1000) = NULL,
    @CREATEDUSER VARCHAR(50) = NULL,
    @ORDERINDEX INT = 0
AS
BEGIN

	IF @INTERPRETDETAILID is null SET @INTERPRETDETAILID = 0
	
    INSERT INTO MD_PRODUCT_ATTRIBUTES
    (
        PRODUCTID,
        ATTRIBUTESGROUPID,
        INTERPRETID,
        INTERPRETDETAILID,
        ISACTIVE,
        ISDELETED,
        CREATEDDATE,
        CREATEDUSER,
        ISSHOWSEARCHRESULT,
        TEXTURL,
        URL,
        ORDERINDEX
    )
    VALUES
    (
        @PRODUCTID,
        @ATTRIBUTESGROUPID,
        @INTERPRETID,
        @INTERPRETDETAILID,
        1,
        0,
        GETDATE(),
        @CREATEDUSER,
        @ISSHOWSEARCHRESULT,
        @TEXTURL,
        @URL,
        @ORDERINDEX
    )
    
END
GO
