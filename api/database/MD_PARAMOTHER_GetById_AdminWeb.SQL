ALTER PROCEDURE [dbo].[MD_PARAMOTHER_GetById_AdminWeb]
    @PARAMOTHERID BIGINT = 0
AS
BEGIN

    SELECT 
            PARAMOTHERID,
            NAMETYPE,
            ISHOUSENUMBER,
            ISPHONENUMBER,
            ISLICENSEPLATE,
            ISACTIVE
    FROM    MD_PARAMOTHER
    WHERE   MD_PARAMOTHER.ISDELETED = 0
    AND     MD_PARAMOTHER.PARAMOTHERID = @PARAMOTHERID
END
GO
