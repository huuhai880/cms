CREATE PROCEDURE AFF_POLICYCOMMISION_Delete_AdminWeb
    @POLICYCOMMISIONID BIGINT = 0,
    @DELETEDUSER VARCHAR(20) = NULL
AS 
BEGIN

    UPDATE  AFF_POLICYCOMMISION
    SET     ISDELETED = 1,
            DELETEDDATE = GETDATE(),
            DELETEDUSER = @DELETEDUSER
    WHERE   POLICYCOMMISIONID = @POLICYCOMMISIONID
END