CREATE PROCEDURE AFF_POLICYCOMMISION_GetDataSelect_AdminWeb
AS 
BEGIN

    SELECT 
            AFF_AFFILIATETYPE.AFFILIATETYPEID,
            AFF_AFFILIATETYPE.AFFILIATETYPENAME,
            AFF_AFFILIATETYPE.ISAFFILIATELEVEL1,
            AFF_AFFILIATETYPE.ISAFFILIATELEVEL2,
            AFF_AFFILIATETYPE.ISDEFAULTTYPE
    FROM    AFF_AFFILIATETYPE
    WHERE   AFF_AFFILIATETYPE.ISACTIVE = 1
    AND     AFF_AFFILIATETYPE.ISDELETED = 0

    SELECT 
            AFF_CONDITION.CONDITIONID,
            AFF_CONDITION.CONDITIONNAME,
            AFF_CONDITION.CONDITIONTYPE, --1: VNƒê, 2: User
            AFF_CONDITION.ISPERSONALSALES,
            AFF_CONDITION.ISGROUPSALES,
            AFF_CONDITION.ISUSERINTRODUCTION,
            AFF_CONDITION.DESCRIPTION
    FROM    AFF_CONDITION
    WHERE   AFF_CONDITION.ISACTIVE = 1
    AND     AFF_CONDITION.ISDELETED = 0

END