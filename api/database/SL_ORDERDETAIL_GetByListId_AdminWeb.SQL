CREATE PROCEDURE SL_ORDERDETAIL_GetByListId_AdminWeb
    @ORDERIDS VARCHAR(2000) = NULL
AS 
BEGIN

        SELECT 
                    SL_ORDERDETAIL.ORDERID,
                    SL_ORDERDETAIL.ORDERDETAILID,
                    IIF(MD_PRODUCT.PRODUCTID IS NULL, PRO_COMBOS.COMBONAME, MD_PRODUCT.PRODUCTNAME) PRODUCTNAME
        FROM        SL_ORDERDETAIL
        LEFT JOIN   MD_PRODUCT
        ON          SL_ORDERDETAIL.PRODUCTID = MD_PRODUCT.PRODUCTID
        AND         MD_PRODUCT.ISDELETED = 0
        AND         MD_PRODUCT.ISACTIVE = 1

        LEFT JOIN   PRO_COMBOS
        ON          PRO_COMBOS.COMBOID = SL_ORDERDETAIL.COMBOID
        AND         PRO_COMBOS.ISDELETED = 0
        AND         PRO_COMBOS.ISACTIVE = 1

        WHERE       SL_ORDERDETAIL.ISDELETED = 0
        AND         SL_ORDERDETAIL.ORDERID IN (SELECT item FROM split(@ORDERIDS, ','))

END