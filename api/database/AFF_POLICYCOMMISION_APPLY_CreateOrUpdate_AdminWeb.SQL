CREATE PROCEDURE AFF_POLICYCOMMISION_APPLY_CreateOrUpdate_AdminWeb
    @MEMBERID BIGINT = 0,
    @POLICYCOMMISIONIDS VARCHAR(1000) = NULL,
    @CREATEDUSER VARCHAR(20) = NULL
AS 
BEGIN

    DELETE AFF_POLICYCOMMISION_APPLY
    WHERE  MEMBERID = @MEMBERID

    INSERT INTO AFF_POLICYCOMMISION_APPLY
    (
        MEMBERID,
        POLICYCOMMISIONID,
        ISACTIVE,
        CREATEDDATE,
        CREATEDUSER,
        ISDELETED
    )
    SELECT 
            @MEMBERID,
            CAST(ITEM AS BIGINT),
            1,
            GETDATE(),
            @CREATEDUSER,
            0
    FROM    dbo.split(@POLICYCOMMISIONIDS, ',')

END